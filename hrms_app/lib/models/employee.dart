class Employee {
  final String name;
  final String initials;
  final String role;
  final String department;
  final String employeeId;
  final String employmentType;

  const Employee({
    required this.name,
    required this.initials,
    required this.role,
    required this.department,
    required this.employeeId,
    required this.employmentType,
  });
}
